<h3>Polluter-pays principle</h3>
The polluter-pays principle is a principle where the polluting party pays for the impact caused to the environment.
 With respect to waste management, this generally refers to the requirement for a waste generator to pay for appropriate disposal 
 of the unrecoverable material.

<h3>History</h3>
Main article: History of waste management
Throughout most of history, the amount of waste generated by humans was insignificant due to low population density and low societal levels of 
the exploitation of natural resources. Common waste produced during pre-modern times was mainly ashes and human biodegradable waste, 
and these were released back into the ground locally, with minimum environmental impact. Tools made out of wood or metal
 were generally reused or passed down through the generations.

However, some civilizations do seem to have been more profligate in their waste output than others. In particular, 
the Maya of Central America had a fixed monthly ritual, in which the people of the village would gather together and burn their rubbish in large dumps.

<h3>Modern era</h3>

Sir Edwin Chadwick's 1842 report The Sanitary Condition of the Labouring Population was influential in securing the passage of the first 
legislation aimed at waste clearance and disposal.
Following the onset of industrialisation and the sustained urban growth of large population centres in England, the buildup of waste in the cities
 caused a rapid deterioration in levels of sanitation and the general quality of urban life. The streets became choked with filth due to the lack of
 waste clearance regulations.[10] Calls for the establishment of a municipal authority with waste removal powers occurred as early as 1751,
 when Corbyn Morris in London proposed that "... as the preservation of the health of the people is of great importance, 
 it is proposed that the cleaning of this city, should be put under one uniform public management, and all the filth be...conveyed by the Thames to 
 proper distance in the country".

However, it was not until the mid-19th century, spurred by increasingly devastating 
cholera outbreaks and the emergence of a public health debate that the first legislation on the issue emerged.
 Highly influential in this new focus was the report The Sanitary Condition of the Labouring Population in 1842 of the social reformer, 
 Edwin Chadwick, in which he argued for the importance of adequate waste removal and management facilities to improve the health and wellbeing of 
 the city's population.

In the UK, the Nuisance Removal and Disease Prevention Act of 1846 began what was to be a steadily evolving process of the provision
 of regulated waste management in London. The Metropolitan Board of Works was the first citywide authority that centralized sanitation 
 regulation for the rapidly expanding city and the Public Health Act 1875 made it compulsory for every household to deposit their weekly waste in 
 "moveable receptacles: for disposalâ€”the first concept for a dust-bin.


Manlove, Alliott & Co. Ltd. 1894 destructor furnace. The use of incinerators for waste disposal became popular in the late 19th century.
The dramatic increase in waste for disposal led to the creation of the first incineration plants, or, as they were then called, "destructors". 
In 1874, the first incinerator was built in Nottingham by Manlove, Alliott & Co. Ltd. to the design of Albert Fryer. However, these were met with 
opposition on account of the large amounts of ash they produced and which wafted over the neighbouring areas.

Similar municipal systems of waste disposal sprung up at the turn of the 20th century in other large cities of Europe and North America. 
In 1895, New York City became the first U.S. city with public-sector garbage management.

Early garbage removal trucks were simply open bodied dump trucks pulled by a team of horses. 
They became motorized in the early part of the 20th century and the first close body trucks to eliminate odours with a dumping lever
 mechanism were introduced in the 1920s in Britain. These were soon equipped with 'hopper mechanisms' where the scooper was loaded at
 floor level and then hoisted mechanically to deposit the waste in the truck. The Garwood Load Packer was the first truck in 1938, to incorporate 
 a hydraulic compactor.

