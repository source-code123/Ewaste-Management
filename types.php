<h1>WASTE TYPES</h1>
Waste management or waste disposal are all the activities and actions required to manage waste from its inception to its final disposal.
 This includes amongst other things collection, transport, treatment and disposal of waste together with monitoring and regulation.
 It also encompasses the legal and regulatory framework that relates to waste management encompassing guidance on recycling.

Waste can take any form that is solid, liquid, or gas and each have different methods of disposal and management.
 Waste management normally deals with all types of waste whether it was created in forms that are industrial, biological, household,
 and special cases where it may pose a threat to human health. It is produced due to human activity such as when factories extract and process raw materials.
 Waste management is intended to reduce adverse effects of waste on health, the environment or aesthetics.

Waste management practices are not uniform among countries (developed and developing nations); regions (urban and rural areas),
 and sectors (residential and industrial).

A large portion of waste management practices deal with municipal solid waste (MSW) which is waste that is created by household, 
industrial, and commercial activity
<h3>Waste hierarchy</h3>
The waste hierarchy refers to the "3 Rs" reduce, reuse and recycle, which classify waste management
 strategies according to their desirability in terms of waste minimisation. The waste hierarchy remains the cornerstone of most waste minimisation strategies.
 The aim of the waste hierarchy is to extract the maximum practical benefits from products and to 
 generate the minimum amount of waste; see: resource recovery. The waste hierarchy is represented as a pyramid because the basic premise 
 is for policy to take action first and prevent the generation of waste. The next step or preferred action is to reduce the generation of waste i.e.
 by re-use. The next is recycling which would include composting. Following this step is material recovery and waste-to-energy.
 Energy can be recovered from processes i.e. landfill and combustion, at this level of the hierarchy. The final action is disposal,
 in landfills or through incineration without energy recovery. This last step is the final resort for waste which has not been prevented,
 diverted or recovered. The waste hierarchy represents the progression of a product or material through the sequential stages
 of the pyramid of waste management. The hierarchy represents the latter parts of the life-cycle for each product.

<h3>Life-cycle of a product</h3>
The life-cycle begins with design, then proceeds through manufacture, distribution, use and then follows through the waste hierarchy's stages of reduce, 
reuse and recycle. Each of the above stages of the life-cycle offers opportunities for policy intervention, to rethink the need for the product, 
to redesign to minimize waste potential, to extend its use. The key behind the life-cycle of a product is to
 optimize the use of the world's limited resources by avoiding the unnecessary generation of waste.

