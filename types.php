 The City's "Pay-As-You-Throw" system charges customers by the volume of landfill-bound materials,
 which provides a financial incentive to separate recyclables and compostables from other discards. 
 The City's Department of the Environment's Zero Waste Program has led the City to achieve 80% diversion, 
 the highest diversion rate in North America. Other businesses such as Waste Industries use a variety of
 colors to distinguish between trash and recycling cans.

<h3>Financial models</h3>
In most developed countries, domestic waste disposal is funded from a national or local tax which may be related to income, or national house value.
 Commercial and industrial waste disposal is typically charged for as a commercial service, often as an integrated charge which includes disposal costs.
 This practice may encourage disposal contractors to opt for the cheapest disposal option such as landfill rather than the environmentally best solution
 such as re-use and recycling. In some areas such as Taipei, the city government charges its households and industries for the volume of rubbish they produce.
 Waste will only be collected by the city council if waste is disposed in government issued rubbish bags. This policy has successfully
 reduced the amount of waste the city produces and increased the recycling rate.
