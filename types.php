Waste collection methods vary widely among different countries and regions.
 Domestic waste collection services are often provided by local government authorities, or by private companies for industrial and commercial waste.
 Some areas, especially those in less developed countries, do not have formal waste-collection systems.

<h3>Waste handling practices</h3>
Curbside collection is the most common method of disposal in most European countries,
 Canada, New Zealand and many other parts of the developed world in which waste is collected at regular intervals by specialised trucks. 
 This is often associated with curb-side waste segregation. In rural areas waste may need to be taken to a transfer station. 
 Waste collected is then transported to an appropriate disposal facility. In some areas, 
 vacuum collection is used in which waste is transported from the home or commercial premises by vacuum along small bore tubes.
 Systems are in use in Europe and North America.

<h3>Main article: Automated vacuum collection</h3>
In some jurisdictions unsegregated waste is collected at the curb-side or from waste transfer stations and then sorted into 
recyclables and unusable waste. Such systems are capable of sorting large volumes of solid waste, salvaging recyclables, and turning the rest
 into bio-gas and soil conditioner. In San Francisco, the local government established its Mandatory Recycling and Composting Ordinance in support
 of its goal of "Zero waste by 2020", requiring everyone in the city to keep recyclables and compostables out of the landfill. The three streams 
 are collected with the curbside "Fantastic 3" bin system â€“ blue for recyclables, green for compostables, and black for landfill-bound
