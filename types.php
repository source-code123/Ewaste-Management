<h3>Recycling</h3>
Main article: Recycling

Waste not the Waste. Sign in Tamil Nadu, India

Steel crushed and baled for recycling
Recycling is a resource recovery practice that refers to the collection and reuse of waste materials such as empty beverage containers.
 The materials from which the items are made can be reprocessed into new products.
 Material for recycling may be collected separately from general waste using dedicated bins and collection vehicles, 
 a procedure called kerbside collection. In some communities, the owner of the waste is required to separate the materials into different bins 
 (e.g. for paper, plastics, metals) prior to its collection. In other communities, all recyclable materials are placed in a single bin for collection,
 and the sorting is handled later at a central facility. The latter method is known as "single-stream recycling.

The most common consumer products recycled include aluminium such as beverage cans, copper such as wire, steel from food and aerosol cans, 
old steel furnishings or equipment, rubber tyres, polyethylene and PET bottles, glass bottles and jars, paperboard cartons, newspapers,
 magazines and light paper, and corrugated fiberboard boxes.

PVC, LDPE, PP, and PS (see resin identification code) are also recyclable. These items are usually composed of a single type of material, 
making them relatively easy to recycle into new products. The recycling of complex products (such as computers and electronic equipment)
 is more difficult, due to the additional dismantling and separation required.

The type of material accepted for recycling varies by city and country. Each city and country has different recycling programs
 in place that can handle the various types of recyclable materials. However, certain variation in acceptance is reflected in the resale value of the
 material once it is reprocessed. In July 2017, the Chinese government announced an import ban of 24 categories of recyclables and solid waste, 
 including plastic, textiles and mixed paper, placing tremendous impact on developed countries globally, which exported directly or indirectly to China.

