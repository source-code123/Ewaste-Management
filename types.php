<h3>Energy recovery</h3>
Main article: Waste-to-energy
Energy recovery from waste is the conversion of non-recyclable waste materials into usable heat, electricity,
 or fuel through a variety of processes, including combustion, gasification, pyrolyzation, anaerobic digestion,
 and landfill gas recovery. This process is often called waste-to-energy. Energy recovery from waste is part of 
 the non-hazardous waste management hierarchy. Using energy recovery to convert non-recyclable waste materials into electricity and heat,
 generates a renewable energy source and can reduce carbon emissions by offsetting the need for energy from fossil sources as well as reduce
 methane generation from landfills.Globally, waste-to-energy accounts for 16% of waste management.

The energy content of waste products can be harnessed directly by using them as a direct combustion fuel,
 or indirectly by processing them into another type of fuel. Thermal treatment ranges from using waste as a fuel source for
 cooking or heating and the use of the gas fuel (see above), to fuel for boilers to generate steam and electricity in a turbine. 
 Pyrolysis and gasification are two related forms of thermal treatment where waste materials are heated to high temperatures with limited oxygen availability.
 The process usually occurs in a sealed vessel under high pressure. Pyrolysis of solid waste converts the material into solid, liquid and gas products.
 The liquid and gas can be burnt to produce energy or refined into other chemical products (chemical refinery). The solid residue (char) can be further 
 refined into products such as activated carbon. Gasification and advanced Plasma arc gasification are used to convert organic materials directly into a
 synthetic gas (syngas) composed of carbon monoxide and hydrogen. The gas is then burnt to produce electricity and steam. An alternative to pyrolysis is
 high temperature and pressure supercritical water decomposition (hydrothermal monophasic oxidation).

